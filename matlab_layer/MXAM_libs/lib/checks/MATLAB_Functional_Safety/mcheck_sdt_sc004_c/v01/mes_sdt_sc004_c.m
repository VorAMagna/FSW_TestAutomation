%% Arithmetical Operators: Data Range of the Output Data Type
%
% [result,cr_items,r_stats] =  mes_sdt_sc004(system,cmd_s)
%
% OUTPUT:
% =======
% result    - check summary(passed/failed/error messages)
% cr_items  - list of individual results of checked items
% r_stats   - struct with statistic s on the check result
%
% INPUT:
% ======
% system    - model/subsystem to be checked
% cmd_s     - check options, syntax /<key>:<val>,
%
% REFERENCED GUIDELINE:
% =====================
% MES Functional Safety Guidelines
% Release 1.7 (21 June 2013)
% Corresponding Guideline: 2.1
%
% DESCRIPTION:
% ============
% Arithmetic blocks with two or more input signals (sum, product, dot
% product) must not use inconsistent or inadequate input data types.
% Therefore, the data range of the output data type must be adequate for
% holding any result of the arithmetic operation.
%
% PASS/FAIL CRITERIA:
% ===================
% The check inspects all Simulink and TargetLink product, sum, and dot
% product blocks. Any inspected block receives a fail, if the data range of
% the output data type is insufficient to cover the range of the worst case
% result of the arithmetic operation.
%
% SOLUTION:
% =========
% Adjust the diverging signal properties accordingly.
% To resolve possible overflows,
%     1) downscale or constrain the range of the input signals, or
%     2) choose a data type of the output signal with a larger range, or
%     3) upscale the output signal
%
% FIX OPERATION:
% ==============
% No automatic fix available.
%
% CHECK PARAMETERS:
% =================
% SDT.sc004_c.IgnoreRangeOfFloatOutput:
%   DATATYPE:
%       boolean
%   DEFAULT VALUE:
%       true (1)
%   DESCRIPTION:
%       If true and blocks output is of type float, block is ignored.
%
% SDT.sc004_c.TrustSaturationSettingOfBlock:
%   DATATYPE:
%       boolean
%   DEFAULT VALUE:
%       true (1)
%   DESCRIPTION:
%       If true, TL Sums and TL Products are ignored if their output maximum
%       and output minimum is set unequal to 0 and not to NaN.
%
% SDT.sc004_c.TrustUserSpecifiedRange:
%   DATATYPE:
%       boolean
%   DEFAULT VALUE:
%       true (1)
%   DESCRIPTION:
%       if true, user-defined ranges (in Targetlink: output.min, output.max,
%       in Stateflow: sf.min, sf.max) are accepted to limit the control
%       input range.
%       If false, the data-limited range of control input is checked, and
%       user-defined min and max values are ignored.
%
% NOTE:
% =====
% - Adjusting the data type of source blocks may require further, secondary
%   adjustments involving other blocks.
% - Scalings affect the accuracy of the data and may lead to inefficient
%   code.
% - When using TargetLink, provide information about the known data range
%   to constrain the input range, whenever possible.
% - With Simulink, use a saturation block to visibly constrain the input
%   data range, instead of using the hidden block property 'Saturate on
%   integer overflow'.
%
% PUBLISHING INFORMATION
% *************************************************************************
% Copyright:    Model Engineering Solutions
% Date:         $Date: 2020/07/31 16:03:10CEST $
% Revision:     $Rev: 15194 $
% *************************************************************************

