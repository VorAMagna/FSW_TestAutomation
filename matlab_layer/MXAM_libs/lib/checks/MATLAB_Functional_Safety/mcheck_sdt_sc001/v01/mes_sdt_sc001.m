%% TargetLink Function: Strong Data Typing at the Interface
%
% [result,cr_items,r_stats] = mes_sdt_sc001(system, cmd_s)
%
% OUTPUT:
% =======
% result    - check summary(passed/failed/error messages)
% cr_items  - list of individual results of checked items
% r_stats   - struct with statistic s on the check result
% for details use 'open mes_check_info'
%
% INPUT:
% ======
% system    - model/subsystem to be checked
% cmd_s     - check options, syntax /<key>:<val>,
% for details use 'open mes_check_info'
%
% REFERENCED GUIDELINE:
% =====================
% MES Functional Safety Guidelines
% Release 1.7 (21 June 2013)
% Corresponding Guideline: 2.1
%
% DESCRIPTION:
% ============
% All subsystems which are defined as TargetLink functions with the
% TL_FUNCTION block have to fulfill the following requirements:
%
% 1. Data type, scaling, and range of each TargetLink Inport which provides
%    a parameter of the respective TL function must be identical with the
%    data type, scaling, and range of its source block.
% 2. Data type, scaling, and range of each TargetLink Outport which returns
%    a value of the TargetLink function must be identical with the data
%    type, scaling and range of its source block.
%
% PASS/FAIL CRITERIA:
% ===================
% A fail is given to each TL_Inport or TL_Outport within function
% subsystems which differs in its data type, scaling or ranges from its
% sourceblock.
%
% If one of the following criteria fails, a fault (prefix f) or a note
% (prefix n) is given for the port/the source and the remaining criteria
% are not checked. The criteria are checked from top to bottom:
%
% 1. (failure) the port is not connected to a source signal
% 2. (note) the source block does not support TargetLink data types. Use the check parameter SDT.IgnoreNonTLSources to deactivate this finding.
% 3. (failure) Matching output port of Stateflow source chart not found.
% 4. (failure) there is no TargetLink data type set for the port or its source
% 5. (failure) the base type of the TargetLink data type of the port or its source is not defined
% 6. (failure) the base types of the port and its source are different
% 7. (failure) the Data Dictionary scaling definition of the port or its source is invalid
% 8. (failure) the LSB value of the port and its source are different
% 9. (failure) the offsets (biases) of the port and its source differ
% 10.(failure) the LSB types (power of two vs. arbitrary) of the port and its source are different
% 11.(failure) the user-defined range constraints ("min", "max") of the port and its source are different. Set the check parameter SDT.IgnoreUndefinedRanges to true to ignore this criterion.
% 11.a) range underflow: minimum of data range of source is below minimum of data range of port
% 11.b) range overflow: maximum of data range of source exceeds maximum of data range of port
% 11.c) user-defined minimum values of port and source are different
% 11.e) user-defined maximum values of port and source are different
%
% SOLUTION:
% =========
% Make sure, that the data type, scaling and its range of a TL Inport or
% TL Outport do not differ from the ones of the corresponding source.
%
% FIX OPERATION:
% ==============
% No automatically fix available.
%
% CHECK PARAMETERS:
% =================
% SDT.sc001.IgnoreNonTLSources:
%   DATATYPE:
%       boolean
%   DEFAULT VALUE:
%       true (1)
%   DESCRIPTION:
%       Controls, if Simulink sources will raise a warning or are ignored.
%
% SDT.sc001.ValidBaseTypeCombinations:
%   DATATYPE:
%       cellstring
%   DEFAULT VALUE:
%       {'UInt8','Int16'; ...
%        'UInt16','Int32'}
%   DESCRIPTION:
%       Each row specifies a valid base type combinations which should be
%       accepted by the check, i.e. {'UInt8','Int16'; UInt16','Int32'}
%       allows the user to combine data type UInt8 with Int16 and UInt16
%       with Int32.
%
% SDT.sc001.IgnoreTLDefaultDataTypes:
%   DATATYPE:
%       boolean
%   DEFAULT VALUE:
%       true (1)
%   DESCRIPTION:
%       Determines if TargetLink sources with a default class and/or
%       unspecified data type/base type are ignored.
%       This applies to TargetLink Constants and Merge blocks with class =
%       'default', for which the datatype is specified by TargetLink at
%       run-time/build-time. In MIL mode, these blocks will have output
%       int16, in SIL mode, the type is adjusted to match the following
%       block.
%
% SDT.sc001.IgnoreUndefinedRanges:
%   DATATYPE:
%       boolean
%   DEFAULT VALUE:
%       false (0)
%   DESCRIPTION:
%       If true, undefined Min/Max values of Stateflow and TargetLink
%       signals raise an error. If false, they will be ignored.
%
% NOTE:
% =====
% - User-defined ranges of boolean and constant sources are not checked.
% - Consistency between TL-outports and their destination blocks is not
%   checked, because the destination block might need to change the data
%   type.
% - This check requires a full featured TL Version.
%
% PUBLISHING INFORMATION
% *************************************************************************
% Copyright:    Model Engineering Solutions
% Date:         $Date: 2020/07/31 16:03:06CEST $
% Revision:     $Rev: 7355 $
% *************************************************************************

