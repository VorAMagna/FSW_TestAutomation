% get the info of the TL autosar runnableInport or outpot block of TL 2.x system.
%
% SYNTAX:
% =======
%   codeDate = mes_getAutosarBlockInfo2(hBlock)
%
% DESCRIPTION:
% ============
% Get the info of the TL autosar RunnableInport or RunnableOutport block.
% In the return struct, the signal type (bus or not bus), the scaling info
% can be found, which can not be obtained by tl_get.
% 
% INPUT ARGUMENTS:
% ================
% -  hBlock          double (no default value)
%                    Handle of the RunnableInport or RunnableOutport block.
%
% RETURN VALUES:
% ==============
% - codeData     struct
%                A struct that has the info of the block
%                codeData.elementData.signals.isBus-- if the output signal is bus, true
%                codeData.elementData.signals.output-- name, type and scaling info of the
%                output signal.
% EXAMPLE(S):
% ===========
% codeData=mes_getAutosarBlockInfo(gcbh);
%
% *************************************************************************
% Copyright:  Model Engineering Solutions
% Date:       $Date: 2020/07/31 16:10:08CEST $
% Revision:   $Rev: 932 $
% *************************************************************************

